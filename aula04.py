# Tipos int e Float
# Int -> numeros inteiros
# o tipo int representa qualquer número positivo ou negativo. Int sem sinal é considerado positivo.
print(11) # int
print(-11) # int
print(0) # int
# Float -> número com ponto flutuante o tipo float representa qualquer nuúmero positivo ou negativo
# com ponto flutuante , float sem sinal é considerado positivo.
print(10.01, 7.23) # float
print(0.0, -1.5) # float
# A função type mostra o tipo que o Python inferiu ao valor.
print(type('Hello Word'))
print(type(0))
print(type(1.6), type(-10), type('oi'))
