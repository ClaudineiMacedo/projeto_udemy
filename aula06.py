# conversão de tipos, coerção type convertion, typecasting, coercion é o ato de converter um tipo em
# outro tipos imutáveis e primitivos: str, int, float, bool.
print(int('1'), type(int('1')))# aqui esta invertendo uma 'str' em um numéro 'int'.
print(type(float('1') + 1))
print(bool(' '))
print(str(11) + 'b')