# if  /  elif   / else
# se  / se não se / se não
entrada = input('Voçê quer "Entra" ou "Sair"? ')

if entrada == 'entar':
    print('Voçê entrou no sistema!')
elif entrada == 'sair':
    print('Voçê saiu do sistema!')
else:
    print('Voçê não digitou nem "entrar" e nem "sair" tente novamente!')


