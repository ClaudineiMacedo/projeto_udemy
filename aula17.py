# if  /  elif   / else
# se  / se não se / se não

condicao1 = False
condicao2 = False
condicao3 = False
condicao4 = True

if condicao1:
    print('Foi atendido a condição 1 ')
elif condicao2:
    print('Foi atendido a condição 2 ')
elif condicao3:
    print('Foi atendido a condição 3 ')
elif condicao4:
    print('Foi atendido a condição 4 ')
else:
    print('Nenhuma das condições foi atendida!')

